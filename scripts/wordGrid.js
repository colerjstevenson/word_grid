function startGame() {
    gameArea.start();
    grid = new Grid(WORD_LENGTH, WORD_LENGTH);
    guessList = new Guesses(WORD_LENGTH, grid.boxHeight*0.9);
    reset = new Button((gameArea.width*2)/3, grid.startY/2, grid.boxWidth, grid.startY/2,"#d6b578dc", "reset");
    help = new Button(gameArea.width/3, grid.startY/2, grid.boxWidth, grid.startY/2,"#d6b578dc", "help");
}

function resetGame(){
    for(var i = 0;  i < guessList.guesses.length; i += 1){
        guessList.guesses[i].setColor("white")
        guessList.guesses[i].letters = [];
    }

    for(var i = 0;  i < grid.boxes.length; i += 1){
        grid.boxes[i].state = FREE;
        grid.boxes[i].color = "white"
    }

    guessList.atGuess = 0;
}

function updateGame() {
    gameArea.clear();
    grid.display();
    guessList.display();
    reset.update();
    help.update();
}

var gameArea = {
    canvas : document.createElement("canvas"),
    start : function() {
       
        this.height = window.innerHeight;
        this.width = (this.height*9)/16
        
        

        this.canvas.width = this.width;
        this.canvas.height = this.height;
        this.context = this.canvas.getContext("2d");
        document.body.insertBefore(this.canvas, document.body.childNodes[0]);
        this.frameNo = 0;
        this.interval = setInterval(updateGame, 40);

    },
    clear : function() {
        this.context.clearRect(0, 0, this.canvas.width, this.canvas.height);
        
    },
    reset : function(){
        
    }
}


function Grid(nrow, ncol){
    var pad = 5;
    var top = gameArea.height/15;

    this.nrows = nrow;
    this.ncols = ncol;
    

    this.boxWidth = Math.min(
        (gameArea.width - (pad * (this.ncols - 1))) / this.ncols,
        ((gameArea.height / 2) - top - (pad * (this.nrows - 1))) / this.nrows
    );
    this.boxHeight = this.boxWidth;

    this.startX = (gameArea.width - (this.boxWidth * this.ncols + pad * (this.ncols - 1))) / 2;
    this.startY = top + (((gameArea.height / 2) - top - (this.boxWidth * this.nrows + pad * (this.nrows - 1))) / 2);

    this.boxes = [];
    var currX = this.startX;
    var currY = this.startY;


    var l = getLetters();



    for(var c = 0; c < this.ncols; c += 1){
        currY = this.startY;
        for(var r = 0; r < this.nrows; r += 1){
            this.boxes.push(new letterBox(this.boxWidth, this.boxHeight, currX + this.boxWidth/2, currY+this.boxHeight/2, l[c*this.ncols + r]))
            currY += this.boxHeight + pad;
        }
        currX += this.boxWidth + pad
    }

    this.display = function(){
        for(var i = 0; i < this.boxes.length; i+=1){
            this.boxes[i].update()
        }
    }

    this.checkClick = function(x, y){
        for(var i = 0; i < this.boxes.length; i+=1){
            if(this.boxes[i].checkClick(x, y)){
                return i;
            }
        }

        return -1;
    }

}



function letterBox(width, height, x, y, letter, color="white") {
    this.width = width;
    this.height = height;    
    this.x = x;
    this.y = y;
    this.letter = letter;
    this.color = color;

    this.state = FREE;

    this.update = function() {
        ctx = gameArea.context;
        if(this.state != USED){
            ctx.fillStyle = this.color;
            ctx.fillRect(this.x-width/2, this.y-height/2, this.width, this.height);
            
            ctx.font = this.width + "px Courier New";
            var textWidth = ctx.measureText(this.letter).width;

            ctx.fillStyle = "black";
            ctx.fillText(this.letter, this.x-(textWidth/2), this.y+(this.height/3));
        }
        
    }

    this.checkClick = function(mX, mY){
        
        if(Math.sqrt((this.x - mX)*(this.x - mX) + (this.y - mY)*(this.y - mY)) < this.width/2){
            return true;
        }else{
            return false
        }


    }

    this.click = function(i){
        if(this.state == FREE && guessList.guesses[guessList.atGuess].letters.length < WORD_LENGTH){
            this.state = PICKED
            this.color = "grey"
            guessList.addLetter(i)
        }else if(this.state == PICKED){
            this.state = FREE
            this.color = "white"
            guessList.addLetter(i)
        }
        
        
    }

}


function WordDisplay(pos, size){
    var pad = 5;
    
    this.x = gameArea.width/2;
    this.y = pos

    this.size = size;

    this.boxes = []
    this.letters =[]
    
    for(var i = this.x - (2*(this.size+pad)); i <= this.x + (2*(this.size+pad)); i += this.size + pad){
        this.boxes.push(new letterBox(this.size, this.size, i, this.y, ""))
    }

    this.display = function(){
        for(var i = 0;  i < this.boxes.length; i += 1){
            if(this.letters.length > i){
                this.boxes[i].letter = grid.boxes[this.letters[i]].letter
            }else{
                this.boxes[i].letter = "";
            }
            
            this.boxes[i].update();
        }

        
    }

    this.setColor = function(color){
        for(var i = 0;  i < this.boxes.length; i += 1){
            this.boxes[i].color = color;
        }
    }

    this.checkGuess = function(){
        if(this.letters.length != 5){
            return false
        }
    }

    

}



function Guesses(num, size){

    var space = 15;
    this.nguesses = num;
    this.size = size;

    this.atGuess = 0;

    this.topPos = (gameArea.height / 2) +(this.size/2) + ((gameArea.height / 2) - (this.size * this.nguesses + space * (this.nguesses - 1))) / 2;
    
    this.guesses = [];

    for(var i = 0; i < num; i +=1){
        this.guesses.push(new WordDisplay(this.topPos + i*(size+space), size));
    }

    this.display = function(){
        for(var i = 0; i < this.guesses.length; i +=1){
            this.guesses[i].display();
        }

        if(this.guesses[this.atGuess].letters.length == WORD_LENGTH){
            this.checkWord()
        }else{
            this.guesses[this.atGuess].setColor("white")
        }
    }

    this.addLetter = function(letter){
        if(this.guesses[this.atGuess].letters.includes(letter)){
            console.log(this.guesses[this.atGuess].letters + " ---> " +  letter)
            var index = this.guesses[this.atGuess].letters.indexOf(letter)
            this.guesses[this.atGuess].letters.splice(index, 1)
            return
        }
        
        if(this.guesses[this.atGuess].letters.length < WORD_LENGTH){
            this.guesses[this.atGuess].letters.push(letter);
        }
    }

    this.checkWord = function(){
        var word = "";
        for(var i = 0; i < this.guesses[this.atGuess].letters.length; i +=1){
            word += grid.boxes[this.guesses[this.atGuess].letters[i]].letter;
        }

        console.log(word)
        if(wordList.includes(word.toLowerCase())){
            this.guesses[this.atGuess].setColor("green");
            this.nextGuess();
        }else{
            this.guesses[this.atGuess].setColor("red")
        }
    }

    this.nextGuess = function(){
        console.log(this.guesses[this.atGuess])
        for(var i = 0; i < this.guesses[this.atGuess].letters.length; i +=1){
            grid.boxes[this.guesses[this.atGuess].letters[i]].state = USED;
        }
        this.atGuess += 1;
    }

    

}

